package de.rwth.i9.palm.model;

/**
 * 
 * <b>Documentation:</b> {@code SMALL} The widget size is 33.3% of container
 * size. {@code MEDIUM} The widget size is 66.7% of container size.
 * {@code LARGE} The widget size is 100% of container size. {@code HALF} The
 * widget size is 50% of container size.
 *
 * @author sigit
 */
public enum WidgetWidth
{
	SMALL, MEDIUM, LARGE, HALF
}
